/**
 * Those are the string definitions of events for MobileFacet.
 *
 * Those events should be bound to the MobileFacet element.
 */
export class MobileFacetEvents {
  /**
   * Executed when facets in their mobile state are opened.
   */
  public static popupOpened = 'popupOpened';
  /**
   * Executed when facets in their mobile state are closed.
   */
  public static popupClosed = 'popupClosed';
}
